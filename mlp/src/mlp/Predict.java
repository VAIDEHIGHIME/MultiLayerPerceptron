    public float[][] Predict()
    {
        matrix mat_operation=new matrix(); 
        
        return(mat_operation.activity(mat_operation.multiplyMatrices(mat_operation.activity(mat_operation.multiplyMatrices(this.input,this.w1,1)),this.w2,1)));
    }
    
    public String findClass(float[][] Predicted_matrix)
    {
        String c = null;
        float max=Float.MIN_VALUE;
        int max_index = 0;
        for(int i=0;i<Predicted_matrix.length;i++)
        {
            if(Predicted_matrix[0][i]>max)
            {
                max=Predicted_matrix[0][i];
                max_index=i;
                        
            }
        }
        if(max_index==0)
        {
            c="Iris-Setosa";
        }
        if(max_index==1)
        {
            c="Iris-Vesicolor";
        }
        if(max_index==2)
        {
            c="Iris-Verginica";
        }
        
        return(c);
    }
   
